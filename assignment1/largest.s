	AREA appcode,CODE,READONLY
    EXPORT __main
		ENTRY 
__main  FUNCTION	
	    MOV R0,#40			;A = 40
		MOV R1,#30			;B = 30
		MOV R2,#20          ;C = 20
		MOV R4,#0x20000000


		CMP R0,R1			;COMPARING A AND B AND STORING MAX IN R3
		BPL AGREATER		;R0> R1 CASE 
		MOV R3,R1			;R1 > R0 CASE
		B CCOMP


AGREATER	MOV R3,R0


CCOMP	CMP R3,R2			;COMPARING R3,C AND STORING MAX IN R3
		BPL FINAL			;C < (GREATER OF A AND B)
		MOV R3,R2			;C > (GREATER OF A AND B)


FINAL 	STR R3,[R4]			;OUTPUT IN 0x20000000
stop    B stop              ; TO STOP THE PROGRAM
     ENDFUNC
     END 